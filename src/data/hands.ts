import type { HandGroup, Hand, TileGroup, SuitColor, TileType } from '../types'

// Helper to create tile groups for a hand
let _id = 0
function tg(
  handId: string,
  display: string,
  tileType: TileType,
  tileValue: string,
  count: number,
  suitColor?: SuitColor
): TileGroup {
  return {
    id: `${handId}-${_id++}`,
    display,
    tileType,
    tileValue,
    count,
    suitColor,
  }
}

function op(handId: string, display: string): TileGroup {
  return tg(handId, display, 'operator', display, 0)
}

function hand(
  id: string,
  groupId: string,
  number: string,
  groups: (id: string) => TileGroup[],
  description: string,
  concealed: boolean,
  points: number
): Hand {
  return { id, groupId, number, tileGroups: groups(id), description, concealed, points }
}

